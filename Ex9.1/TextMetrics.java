public class TextMetrics {
    private int espacos;
    private int pontos;
    private int letraA;

    public void setEspacos(int espacos) {
        this.espacos = espacos;
    }

    public int getEspacos() {
        return espacos;
    }

    public void setPontos(int pontos) {
        this.pontos = pontos;
    }

    public int getPontos() {
        return pontos;
    }

    public void setLetraA(int letraA) {
        this.letraA = letraA;
    }

    public int getLetraA() {
        return letraA;
    }
}
